# Solicitar tres números al usuario
Num1 = float(input("Ingresa el primer número: "))
Num2 = float(input("Ingresa el segundo número: "))
Num3 = float(input("Ingresa el tercer número: "))

# Calcular la suma de los tres números
suma = Num1 + Num2 + Num3

# Calcular el promedio de los tres números
promedio = suma / 3

# Imprimir la suma y el promedio
print(f"La suma de los tres números es: {suma}")
print(f"El promedio de los tres números es: {promedio}")
